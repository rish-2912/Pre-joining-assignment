package basics;

public class Datatypes {
	// Java has 8 primitive data types

	// 1 byte integer literal
	byte data_1;

	// 2 byte integer literal
	short data_2;

	// 4 byte integer literal
	int data_3;

	// 8 byte integer literal
	long data_4;
	float data_5;
	double data_6;
	char data_7;
	boolean data_8;

}
